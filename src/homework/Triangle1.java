package homework;

public class Triangle1 {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            for (int j = i; j >=0 ; j--) {
                System.out.print("*");
            }
            System.out.println();
            }
        for (int a = 4; a >0 ; a--) {
            for (int b = a; b >0 ; b--) {
                System.out.print("*");
            }
            System.out.println();
        }
        }
    }
